namespace EasyAppDev.Blazor.AutoComplete.Options;

/// <summary>
/// Component size variants that adjust spacing proportionally
/// </summary>
public enum ComponentSize
{
    /// <summary>
    /// Compact size with reduced spacing (25% smaller)
    /// </summary>
    Compact = 0,

    /// <summary>
    /// Default size with standard spacing
    /// </summary>
    Default = 1,

    /// <summary>
    /// Large size with increased spacing (25% larger)
    /// </summary>
    Large = 2
}
